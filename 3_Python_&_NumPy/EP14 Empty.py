#Empty
'''
array ที่ไม่ระบุสมาชิก โดย function Empty
function Empty เป็นการสร้าง array เปล่าวข้อมูลข้างในจะสุ่ม
เน้นสร้างโครงสร้าง ข้อมูลข้างในจะสุ่ม
จะสร้างเร็วกว่าแบบอื่นๆ
'''
import numpy as np
print(np.empty([3,4]))
print('')
print(np.empty([3,4]))
print('')
print(np.empty(3))
print('000')
print(np.empty([2,3,4]))

