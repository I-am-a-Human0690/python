#เช็คคำขึ้นต้น
b="นายสิทธิกร"
print(b.startswith("นาย"))
#หรือทำเป็นเงื่อไข
if b.startswith("นาย"):
    print("คำนำหน้าเป็นนาย")
else:
    print("คำนำหน้าเป็นเด็กชาย")
#เช็คคำลงท้าย
c="11223307"
print(c.endswith("7"))
