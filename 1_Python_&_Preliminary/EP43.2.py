#การเข้าถึงข้อมูลใน list
#ตัวอย่าง
#   a     b     c     d     e
#  [0]   [1]   [2]   [3]   [4]
#  [-5]  [-4]  [-3]  [-2]  [-1]
number1=[1,2,3,4]#list มีชมาชิก 1-4
print(number1[0])
print(number1[-1])
print(number1[0]+number1[1])
print(number1[:2])
print(number1[0:2])
print(number1[1:3])
print(number1[-4:-1])
print(number1[-4:])
print(number1[:-1])
